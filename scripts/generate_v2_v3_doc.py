KOR_FONT = "맑은 고딕"
LAT_FONT = "Calibri"
MONO_FONT = "Consolas"


def _set_run_fonts(run, latin=LAT_FONT, korean=KOR_FONT):
    """ascii/hAnsi(라틴) + eastAsia(한글)을 같이 지정.
    eastAsia 미지정 시 한글이 □/?로 표시되는 문제 방지."""
    rPr = run._element.get_or_add_rPr()
    rFonts = rPr.find(qn("w:rFonts"))
    if rFonts is None:
        rFonts = OxmlElement("w:rFonts")
        rPr.append(rFonts)
    rFonts.set(qn("w:ascii"), latin)
    rFonts.set(qn("w:hAnsi"), latin)
    rFonts.set(qn("w:cs"), latin)
    rFonts.set(qn("w:eastAsia"), korean)


def H(doc, text, level):
    h = doc.add_heading(text, level=level)
    for run in h.runs:
        _set_run_fonts(run)
    return h


def P(doc, text, bold=False, italic=False, size=11, color=None):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = bold
    run.italic = italic
    run.font.size = Pt(size)
    if color:
        run.font.color.rgb = RGBColor(*color)
    _set_run_fonts(run)
    return p


def B(doc, text, level=0):
    p = doc.add_paragraph(style="List Bullet")
    p.paragraph_format.left_indent = Cm(0.5 + 0.5 * level)
    run = p.add_run(text)
    run.font.size = Pt(10.5)
    _set_run_fonts(run)
    return p


def Q(doc, quote, source=None):
    """수험생 1인칭 멘트 인용용."""
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Cm(1.0)
    run = p.add_run(f"“{quote}”")
    run.italic = True
    run.font.size = Pt(10.5)
    run.font.color.rgb = RGBColor(0x6B, 0x72, 0x80)
    _set_run_fonts(run)
    if source:
        p2 = doc.add_paragraph()
        p2.paragraph_format.left_indent = Cm(1.0)
        r2 = p2.add_run(f"— {source}")
        r2.font.size = Pt(9)
        r2.font.color.rgb = RGBColor(0x9C, 0xA3, 0xAF)
        _set_run_fonts(r2)
    return p


def reason_block(doc, title, why_lines, intent_line):
    """제목 + 이유 글머리표 + 인식시키려는 메시지 한 줄."""
    p = doc.add_paragraph()
    r = p.add_run(title)
    r.bold = True
    r.font.size = Pt(11.5)
    r.font.color.rgb = RGBColor(0x1F, 0x29, 0x37)
    _set_run_fonts(r)
    for line in why_lines:
        B(doc, line)
    p2 = doc.add_paragraph()
    p2.paragraph_format.left_indent = Cm(0.5)
    r2 = p2.add_run(f"→ 인식시키려는 것: {intent_line}")
    r2.font.size = Pt(10.5)
    r2.font.color.rgb = RGBColor(0x05, 0x96, 0x69)
    r2.bold = True
    _set_run_fonts(r2)
    doc.add_paragraph()  # spacing


